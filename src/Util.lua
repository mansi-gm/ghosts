function GenerateQuads(atlas, tilewidth, tileheight)

    -- initialized variables for the number of rows and columns
    local sheetWidth = atlas:getWidth() / tilewidth
    local sheetHeight = atlas:getHeight() / tileheight

    local sheetCounter = 1
    local spritesheet = {}

    -- using the sheetWidth and sheetHeight, each quad (representing a tile) gets 
    -- added to a one-dimensional array
    for y = 0, sheetHeight - 1 do
        for x = 0, sheetWidth - 1 do
            -- sheetCounter will be used as the index to refer to each tile in the array
            spritesheet[sheetCounter] =
                love.graphics.newQuad(x * tilewidth, y * tileheight, tilewidth,
                tileheight, atlas:getDimensions())
            sheetCounter = sheetCounter + 1
        end
    end

    return spritesheet
end

